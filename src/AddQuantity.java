    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        int selectedOption = JOptionPane.showConfirmDialog(null, "Do you really want to increase quantity?", "Confirm Add", JOptionPane.YES_NO_OPTION);
        if (selectedOption == JOptionPane.YES_OPTION){
            try{
                    int quantity = Integer.parseInt(txtQuantity.getText());
                    Class.forName("org.apache.derby.jdbc.EmbeddedDriver");
                    java.sql.Connection con = DriverManager.getConnection("jdbc:derby://localhost:1527/WarehouseManagement", "wms", "wms");
                    java.sql.Statement stmt = con.createStatement(); 
                    String sql = "UPDATE STOCK SET STOCKQUANTITY = STOCKQUANTITY + " + quantity + " WHERE STOCKID = '" + stockID + "'" ;
                    stmt.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "A quantity of " + quantity + " has been added to " + stockID);
                    this.dispose();
                }catch (Exception e){
                    JOptionPane.showMessageDialog(null, e);
                }
        }
    }//GEN-LAST:event_btnAddActionPerformed

    
    
    
    
