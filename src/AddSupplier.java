    private void btnAddSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddSupplierActionPerformed
        if (validID(txtSupplierID.getText())){
            int selectedOption = JOptionPane.showConfirmDialog(null, "Are you sure you want to add this supplier?", "Confirm Add", JOptionPane.YES_NO_OPTION);
            if (selectedOption == JOptionPane.YES_OPTION){
                try{  
                    String supplierID = txtSupplierID.getText();
                    String name = txtSupplierName.getText();
                    String address = txtAddress.getText();
                    String phoneNo = txtTelephoneNo.getText();;
                    Class.forName("org.apache.derby.jdbc.EmbeddedDriver");
                    java.sql.Connection con = DriverManager.getConnection("jdbc:derby://localhost:1527/WarehouseManagement", "wms", "wms");
                    java.sql.Statement stmt = con.createStatement(); 
                    String sql = "INSERT INTO SUPPLIER (SUPPLIERID, SUPPLIERNAME, SUPPLIERPHONENO, SUPPLIERADDRESS) VALUES ('" 
                            + supplierID + "','" + name + "','" + phoneNo + "','" + address + "')";
                    stmt.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "The supplier has been successfully added");
                    this.dispose();
                }catch (Exception e){
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "SupplierID must contains 3 letters and 4 digits.", "Error", JOptionPane.ERROR_MESSAGE);
            txtSupplierID.setText(null);
        }
    }//GEN-LAST:event_btnAddSupplierActionPerformed

    
    
    
    
