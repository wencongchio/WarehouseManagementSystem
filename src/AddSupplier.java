    private static boolean validID(String userID){
        int digitCount = 0;
        int letterCount = 0;
        for (int i=0;i<userID.length();i++){
            if (Character.isLetter(userID.charAt(i)))
                letterCount++;
            else if (Character.isDigit(userID.charAt(i))){
                digitCount++;
            }
            else{
                return false;
            }
        }
        if (letterCount == 3 && digitCount == 4){
            return true;
        }
        else{
            return false;           
        }
    }

    
    
    
    
